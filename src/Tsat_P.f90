! ---------------------------------------------------------
! 1. Температура насыщения от давления (T_sat в °C от P в Па)
! Официальное уравнение IAPWS R7-97 (уравнение 31)
! Действительно для: 611.213 Pa <= P <= 22.064 MPa
! ---------------------------------------------------------
subroutine Tsat_P(T,P) 
implicit none
real(8) :: P, T
real(8) :: P_MPa, beta, E, F, G, D, T_K
real(8) :: sqrt_term, numerator
real(8) :: n1, n2, n3, n4, n5, n6, n7, n8, n9, n10


! Проверка диапазона Внимание: давление вне диапазона валидности уравнения
if (P < 611.213 .or. P > 22.064e6) then
    write(*,*) '(611.213 Pa - 22.064 MPa)'
end if



! Коэффициенты из Table 34 IAPWS R7-97
n1 =  0.11670521452767e4
n2 = -0.72421316703206e6
n3 = -0.17073846940092e2
n4 =  0.12020824702470e5
n5 = -0.32325550322333e7
n6 =  0.14915108613530e2
n7 = -0.48232657361591e4
n8 =  0.40511340542057e6
n9 = -0.23855557567849
n10 = 0.65017534844798e3



P_MPa = P / 1.0e6  ! Переводим в МПа



! 1. Вычисляем beta (уравнение 29a)
beta = (P_MPa)**0.25

! 2. Вычисляем E, F, G
E = beta**2 + n3*beta + n6
F = n1*beta**2 + n4*beta + n7
G = n2*beta**2 + n5*beta + n8

! 3. Вычисляем D (знаменатель)
sqrt_term = sqrt(F**2 - 4.0*E*G)
if (abs(-F - sqrt_term) > tiny(1.0)) then
    D = (2.0*G) / (-F - sqrt_term)
else
    ! Обработка особого случая (маловероятно при валидных давлениях)
    D = 0.0
end if

! 4. Вычисляем температуру в Кельвинах (уравнение 31)
sqrt_term = sqrt((n10 + D)**2 - 4.0*(n9 + n10*D))
T_K = (n10 + D - sqrt_term) / 2.0  ! T* = 1 K, поэтому опускаем

! 5. Переводим в °C
T = T_K - 273.15
 
end subroutine Tsat_P
